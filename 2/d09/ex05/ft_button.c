int	ft_button(int i, int j, int k)
{
	if ((i > k && i < j) || (i > j && i < k))
		return (i);
	else if ((j > k && j < i) || (j > i && j < k))
		return (j);
	else if ((k > j && k < i) || (k > i && k < j))
		return (k);
	else
		return (0);
}
