int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < i)
		return (0);
	if (nb == 2)
		return (1);
	if (nb > 2147483647 && nb < -2147483648)
		return (0);
	while (i <= (nb / i))
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (1)
	{
		if (ft_is_prime(nb) == 1)
			return (nb);
		nb++;
	}
}
