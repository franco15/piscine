char	*ft_strrev(char *str)
{
	int		l;
	int		i;
	char	c;

	l = 0;
	i = 0;
	while (str[l])
		l++;
	l--;
	while (i < l)
	{
		c = str[l];
		str[l] = str[i];
		str[i] = c;
		i++;
		l--;
	}
	return (str);
}
