char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (to_find[j] == '\0')
			break ;
		else if (str[i] == to_find[j])
		{
			i++;
			j++;
		}
		else
			i++;
	}
	return (str + i - j);
}
