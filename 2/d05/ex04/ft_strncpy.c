char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;
	int j;

	i = 0;
	j = n;
	while (*src && i < j)
	{
		dest[i] = src[i];
		i++;
	}
	while (src[i] != '\0')
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
